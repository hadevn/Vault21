const char* findName(DWORD pSyscallIndex)
{
	int n = sizeof(sysindex) / sizeof(*sysindex);

	int i = 0;
	bool isFound = false;
	while (i < n)
	{
		if (sysindex[i] == pSyscallIndex)
		{
			isFound = true;
			break;
		}
		i++;
	}

	if (isFound)
	{
		return syscallNames[i];
	}

	return nullptr;
}
